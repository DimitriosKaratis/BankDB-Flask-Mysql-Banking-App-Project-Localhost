-- Ερώτημα 5: Ενεργά δάνεια με χρέος μεγαλύτερο του 500, αλλά όχι μεγαλύτερο του 5.000
-- Περιγραφή: Το ερώτημα αυτό αποσκοπεί στην εύρεση όλων των ενεργών δανείων με χρέος μεγαλύτερο του 500, αλλά όχι μεγαλύτερο του 5.000


USE BankDB;

SELECT 
    LoanID, 
    ExpirationDate, 
    Type, 
    Amount, 
    Debt
FROM loan_debts
-- Το WHERE Debt > 500 AND Debt <= 5000 
-- αντιστοιχεί στη διαφορά των δύο συνόλων της άλγεβράς σου
WHERE Debt > 500 AND Debt <= 5000;
